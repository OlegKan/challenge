package com.simplaapliko.challenge.ui.overview.adapder;

import android.support.annotation.Nullable;
import android.support.v7.util.DiffUtil;

import com.simplaapliko.challenge.domain.model.Profile;

import java.util.List;

public class ProfileDiffCallback extends DiffUtil.Callback{

    private List<Profile> oldList;
    private List<Profile> newList;

    public ProfileDiffCallback(List<Profile> newList, List<Profile> oldList) {
        this.newList = newList;
        this.oldList = oldList;
    }

    @Override
    public int getOldListSize() {
        return oldList.size();
    }

    @Override
    public int getNewListSize() {
        return newList.size();
    }

    @Override
    public boolean areItemsTheSame(int oldItemPosition, int newItemPosition) {
        return oldList.get(oldItemPosition).getId() == newList.get(newItemPosition).getId();
    }

    @Override
    public boolean areContentsTheSame(int oldItemPosition, int newItemPosition) {
        return oldList.get(oldItemPosition).equals(newList.get(newItemPosition));
    }

    @Nullable
    @Override
    public Object getChangePayload(int oldItemPosition, int newItemPosition) {
        return super.getChangePayload(oldItemPosition, newItemPosition);
    }
}
